#include "./math.hpp"

int_fast32_t add(int_fast32_t a, int_fast32_t b) {
    return a + b;
}

int_fast32_t subtract(int_fast32_t a, int_fast32_t b) {
    return a - b;
}

int_fast32_t multiplication(int_fast32_t a, int_fast32_t b) {
    return a * b;
}

int_fast32_t division(int_fast32_t a, int_fast32_t b) {
    return a / b;
}
